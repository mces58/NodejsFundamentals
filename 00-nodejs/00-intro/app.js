/*
! Node Js Nedir?
- Node.js, JavaScript programlama dilini sunucu tarafında çalıştırmak ve ağ uygulamaları geliştirmek için kullanılan bir platformdur. Geleneksel olarak JavaScript, tarayıcı ortamında çalıştırılırken, Node.js sayesinde JavaScript sunucu tarafında da çalıştırılabilir hale gelmiştir.

- Node.js, Google'ın V8 JavaScript motorunu temel alır. Bu motor, yüksek performanslı ve verimli bir şekilde JavaScript kodunu çalıştırmak için kullanılır. Node.js'in en önemli özelliği, olay tabanlı, asenkron programlamayı desteklemesidir. Bu, aynı anda birden fazla işlemi eşzamanlı olarak işlemek ve bloke edici (blocking) işlemlerin diğer işlemleri engellemesini önlemek için kullanılır.

! Node.js'in bazı temel özellikleri şunlardır:
? 1-Hızlı İşlem: V8 motorunun kullanımı sayesinde yüksek performanslı işlemler gerçekleştirebilir.

? 2-Olay Tabanlı ve Asenkron Programlama: I/O işlemleri gibi beklemesi gereken işlemler için asenkron yapı kullanılarak diğer işlemleri bloke etmez.

? 3-Modüler Yapı: Node.js, modüler bir yapının kullanılmasını teşvik eder. Bu da kodun daha düzenli ve yönetilebilir olmasını sağlar.

? 4-Paket Yönetimi: Node.js'in en popüler paket yöneticisi olan npm (Node Package Manager) sayesinde, hazır modüller ve kütüphaneler kolayca kullanılabilir.

? 5-Çapraz Platform Desteği: Node.js, farklı işletim sistemlerinde (Windows, macOS, Linux) çalışabilir.

? 6-API Geliştirme: Web sunucuları, API'lar ve ağ uygulamaları geliştirmek için sıkça kullanılır.

? 7-Veritabanı Erişimi: Node.js, çeşitli veritabanlarına erişim sağlayan sürücüleri ve kütüphaneleri destekler.

- Node.js, web sunucuları oluşturmanın yanı sıra komut satırı araçları, otomasyon görevleri ve diğer çeşitli uygulamalar için de kullanılabilir. Popüler birçok web framework'ü de (örneğin Express.js) Node.js üzerinde geliştirilmiştir.
*/

